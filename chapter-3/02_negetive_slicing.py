name = "Abu Bokor"

print(name[0:3])     # Slices from index 0 to 2 (3 is exclusive)
print(name[-4:-1])   # Slices from the 4th last character to the 2nd last
print(name[:4])      # Slices from the start to index 3
print(name[1:4])     # Slices from index 1 to 3
print(name[1:])      # Slices from index 1 to the end
print(name[1:9])     # Slices from index 1 to 8 (9 is exclusive, but it's beyond the string length)
